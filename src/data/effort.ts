export function getEffortZonesForAge(age: number): EffortZone[] {
	return effortZones.filter((zone) => zone.age === age);
}

export function getEffortZone(
	age: number,
	effort: EffortType,
): EffortZone | undefined {
	return effortZones.find((zone) => zone.age === age && zone.effort === effort);
}
